fun main(){
    // A variable is a container that stores data or values
    // In kotlin we have two main types of initializers i.e.: var and val
    // val => immutable (unchangeable) but var => mutable (chnageable)

    val name = "Victor Makau" // string=> a series of character inside of quotes
    val number1 = 5 // Integer => whole number
    val number2 = 40.6 // Double=> number with decimal
    val letter = "K" // char=> it is a single letter of the alphabet
    val isRegistered = true // Boolean => It evaluate to either true or false

    println(name)
    println(number1)
    println(number2)
    println(letter)
    println(isRegistered)
} 