fun main(){
    // if...else if... else conditional statement
    // This is used to evaluate multiple conditions. the else part get executed when all the conditions givenn evaluate to false

    val number = 21

    if (number > 0){
        println("The number is positive.")
    }
    else if (number == 0){
        println("The number is zero")
    }
    else{
        println("The number is negative.")
    }
}